                        <p class="">The client wanted to develop a cross-platform application for iOS and Android handheld devices that would allow food distributors across the US and Canada to streamline their operations. The main mission was to deliver pickers with an application that would allow them to operate more efficiently, easing the sales order and barcode scanning processes.
                                Engineering the application to accurately read barcodes was a project imperative; including this functionality ensures accurate traceability. Customers would have the means to access a product's information, and vendors would be aware of the status of their orders throughout the entirety of the supply chain process.
                                The client had envisioned an application that facilitated a more automated warehouse environment. At the client's request, the application also needed to be able to perform certain jobs functions for the allocation and/or non-allocation of orders, lot control, and catch weight items. 
                                It was paramount that the application could assist pickers in identifying orders that have already been allocated and those that have not, as well as a combination of the two where only some items have been allocated. With the help of this application, pickers would be able to specify a lot (or confirm the lot if pre-allocated) at the time of picking.
                                Additionally, to simplify the picking process, the lot control feature needed to have an option to be turned off if operations in a warehouse do not require lots tracking. The catch weight items feature would leverage weight barcodes to process items quickly.
